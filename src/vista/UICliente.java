    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         try (BufferedWriter escritor = new BufferedWriter(new FileWriter("datos.txt"))) {
            for (Object elemento : listaClientes) {
                escritor.write(elemento.toString());
                escritor.newLine();
            }
        } catch (IOException e) {
            System.out.println("Error al guardar la lista en el archivo: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

